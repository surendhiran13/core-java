package operators;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a = 10, b = 20;
		int c = a + b;// addition operands
		int d = a - b;// subtraction operands
		int e = a * b;// multiplication operands
		int f = a / b;// division operands
		int g = a % b;// modulus operands
		a++;// increment
		b--;// decrement
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(a++);
		System.out.println(a--);
	}

}
