package dataTypes;

public class DataTypes {
	public static void main(String[]args) {
		byte number=120;
		short num=12227;
		int numbers=300654;
		long no=12345768L;
		float myNum=1.23f;
		double myNo=22.44;
		char name='a';
		char a=87;
		boolean javaIsFun=true;
		String cricket="favourite game";
		System.out.println("byte value :"+number);
		System.out.println("short value :"+num);
		System.out.println("int value:"+numbers);
		System.out.println("long value:"+no);
		System.out.println("float value:"+myNum);
		System.out.println("double value:"+myNo);
		System.out.println("char value:"+name);
		System.out.println("ASCII value of 87 is:"+a);
		System.out.println("boolean value:"+javaIsFun);
		System.out.println("String value:"+cricket);
	}

}
